package JavaDersi12_whileLoops;

public class Main {

	public static void main(String[] args) {

		int i = 1;

		// while loops
		while (i < 10) {
			System.out.println(i);
			i++;

		}
		System.out.println("while loops finished.");
	
	
	//do while loops
	
	int j=10;
	do {
		System.out.println(j);
		j+=2;
    }
	
	while(j<10);
		
	System.out.println("do while loops finished.");
}
}

