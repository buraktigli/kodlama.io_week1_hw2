package JavaDersi11_forLoops;

public class Main {

	public static void main(String[] args) {
 
		System.out.println("1den 9a kadar sayilar");
		for (int i = 1; i < 10; i ++) {
			System.out.println(i);
		}
		System.out.println("1den 9a kadar cift sayilar");
		
		for (int i = 2; i < 10; i += 2) {
			System.out.println(i);
		}
		System.out.println("1den 9a kadar tek sayilar");
		
		for (int i = 1; i < 10; i += 2) {
			System.out.println(i);
		}
		
		System.out.println("dongu bitti.");
	}

}
